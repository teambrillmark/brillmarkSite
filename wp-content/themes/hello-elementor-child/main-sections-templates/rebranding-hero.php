function waitForElement(selector, trigger) {
        var interval = setInterval(function () {
        if (
            document &&
            document.querySelector(selector) &&
            document.querySelectorAll(selector).length > 0
        ) {
            clearInterval(interval);
            trigger();
        }
        }, 50);
        setTimeout(function () {
        clearInterval(interval);
        }, 25000);
}
function waitForSwiper(trigger) {
        var interval = setInterval(function () {
            if (typeof window.Swiper != "undefined") {
                clearInterval(interval);
                trigger();
            }
        }, 50);
        setTimeout(function () {
            clearInterval(interval);
        }, 25000);
}


function init(){
    waitForSwiper(function(){
        const swiperContainer = document.querySelector(".bm-testimonial .bm-testmonial-wrapper");
        if (swiperContainer) {
            new Swiper(swiperContainer, {
                slidesPerView: 1,
                loop: false,
                spaceBetween: 20,
                navigation: {
                    nextEl: '.swiper-button-next',  // Add your next button selector
                    prevEl: '.swiper-button-prev',  // Add your previous button selector
                },
            });
        }
    })
}

    // Function to get URL parameter by name
function getUrlParameter(name) {
    name = name.replace(/[\[]/, '\\[').replace(/[\]]/, '\\]');
    var regex = new RegExp('[\\?&]' + name + '=([^&#]*)');
    var results = regex.exec(location.search);
    return results === null ? '' : decodeURIComponent(results[1].replace(/\+/g, ' '));
  }
  
// Function to close the popup and store a session flag
function closePopup() {
  if(document.querySelector('body')){
    document.querySelector('body').classList.add('popup-remove')
  }
  sessionStorage.setItem('popupClosed', 'true');
}

// Initialize popup if 'utm_source' is present and session flag is not set
function init2() {
  var utmSource = getUrlParameter('utm_source');
  var popupClosed = sessionStorage.getItem('popupClosed');

  if (utmSource && !popupClosed) {
    document.querySelector('body').classList.add('show-popup')


    // Add click event listener to the close button
    document.querySelector('.bm-close').addEventListener('click', closePopup);

    // Add event listener to close popup when clicking outside of popup-container
    document.querySelector('.popup-container-outer').addEventListener('click', function (event) {
      if (event.target === document.querySelector('.popup-container-outer')) {
        closePopup();
      }
    });
  }
}

  
waitForElement('.bm-popup', init2)
waitForElement('.bm-testimonial .bm-testmonial-wrapper .swiper-slide .bm-slider-img', init)
