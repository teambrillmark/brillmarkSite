<script>
     document.querySelectorAll('.brillmark-tab-innercontent a').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault(); // Prevent default anchor click behavior
                const targetId = this.getAttribute('data-id'); // Get the target ID
                const targetElement = document.querySelector(targetId); // Select the target element

                // Scroll to the target element
                if (targetElement) {
                    window.scroll(0, findPosition(targetElement));
                }
            });
        });

        function findPosition(obj) {
            let currentTop = 0;
            if (obj.offsetParent) {
                do {
                    currentTop += obj.offsetTop; // Add the offset top of each parent element
                } while ((obj = obj.offsetParent));
                return currentTop; // Return the total offset top
            }
            return currentTop; // Return 0 if there's no offset parent
        }
</script>